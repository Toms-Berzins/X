export type Theme = typeof theme;

// Utility types
export type ColorVariant = keyof typeof theme.colors;
export type ColorShade = keyof typeof theme.colors.primary;
export type Spacing = keyof typeof theme.spacing;
export type BorderRadius = keyof typeof theme.borderRadius;
export type Shadow = keyof typeof theme.shadows;
export type Breakpoint = keyof typeof theme.breakpoints;

// Utility functions
export const getColor = (variant: ColorVariant, shade: ColorShade) => {
  return theme.colors[variant][shade];
};

export const getSpacing = (size: Spacing) => {
  return theme.spacing[size];
};

export const getBorderRadius = (size: BorderRadius) => {
  return theme.borderRadius[size];
};

export const getShadow = (size: Shadow) => {
  return theme.shadows[size];
};

export const getBreakpoint = (size: Breakpoint) => {
  return theme.breakpoints[size];
}; 